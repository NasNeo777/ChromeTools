// ---------- 流式调用 DeepSeek ----------
async function summarizeStream(transcript, comments, meta, settings, mode, onDelta) {
  const clip = (s, n) => (s && s.length > n ? s.slice(0, n) + " …（过长已截断）" : (s || ""));
  const MAX = 48000;

  let userContent;
  if (mode === "comments") {
    userContent =
      `【视频文字稿】\n${clip(transcript, 30000)}\n\n` +
      `【观众评论】\n${clip(comments, 16000)}`;
  } else {
    userContent = clip(transcript, MAX);
  }

  const system =
    mode === "translate"
      ? buildTranslatePrompt(settings.language, meta)
      : mode === "critique"
      ? buildCritiquePrompt(settings.language, meta)
      : mode === "comments"
      ? buildCommentsPrompt(settings.language, meta)
      : buildSummaryPrompt(settings.style, settings.language, meta);

  const resp = await fetch(DEEPSEEK_ENDPOINT, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${settings.apiKey}`
    },
    body: JSON.stringify({
      model: settings.model || "deepseek-chat",
      messages: [
        { role: "system", content: system },
        { role: "user", content: userContent }
      ],
      temperature: mode === "translate" ? 1.2 : 0.3,
      stream: true
    })
  });

  if (!resp.ok) {
    const errText = await resp.text().catch(() => "");
    throw new Error(`DeepSeek API ${resp.status}: ${errText.slice(0, 200)}`);
  }

  const reader = resp.body.getReader();
  const decoder = new TextDecoder();
  let buf = "";
  while (true) {
    const { done, value } = await reader.read();
    if (done) break;
    buf += decoder.decode(value, { stream: true });
    const lines = buf.split("\n");
    buf = lines.pop();
    for (const raw of lines) {
      const line = raw.trim();
      if (!line.startsWith("data:")) continue;
      const payload = line.slice(5).trim();
      if (payload === "[DONE]") continue;
      try {
        const j = JSON.parse(payload);
        const delta = j.choices?.[0]?.delta?.content;
        if (delta) onDelta(delta);
      } catch (e) { /* 忽略不完整片段 */ }
    }
  }
}

// ---------- Port：分步进度 + 流式输出 ----------
chrome.runtime.onConnect.addListener((port) => {
  if (port.name !== "summarize") return;

  port.onMessage.addListener(async (msg) => {
    if (msg?.type !== "START") return;
    try {
      const settings = await getSettings();
      if (!settings.apiKey) {
        safePost(port, { type: "error", error: "未设置 DeepSeek API Key，请点击扩展图标配置。" });
        return;
      }
      const transcript = msg.transcript || "";
      const comments = msg.comments || "";
      const meta = msg.meta || {};
      const mode = ["translate", "critique", "comments"].includes(msg.mode) ? msg.mode : "summary";
      if (!transcript) {
        safePost(port, { type: "error", error: "文字稿为空。" });
        return;
      }
      await summarizeStream(transcript, comments, meta, settings, mode, (delta) => {
        safePost(port, { type: "chunk", delta });
      });
      safePost(port, { type: "done", meta, chars: transcript.length });
    } catch (e) {
      safePost(port, { type: "error", error: String(e?.message || e) });
    }
  });
});

function safePost(port, m) {
  try { port.postMessage(m); } catch (e) { /* 端口已关闭 */ }
}

// 设置查询（popup 仍走普通消息）
chrome.runtime.onMessage.addListener((msg, sender, sendResponse) => {
  if (msg?.type === "GET_SETTINGS") {
    getSettings().then((s) => sendResponse(s));
    return true;
  }
});
